def pick_daily_context():
    tool_name, tool_use, tool_test = random.choice(TOOLS_OF_THE_DAY)
    return {
        "focus": random.choice(FOCUS_LIST),
        "card": random.choice(DAY_CARDS),
        "tool_name": tool_name,
        "tool_use": tool_use,
        "tool_test": tool_test,
        "content_idea": random.choice(CONTENT_IDEAS),
        "chinor_idea": random.choice(CHINOR_IDEAS),
        "fact": random.choice(FACTS_OF_DAY),
    }


def build_ai_news_raw(news):
    if not news:
        return "Новости ИИ не загрузились."

    lines = []
    for i, item in enumerate(news, start=1):
        lines.append(
            f"{i}. {item.get('title', '')}\n"
            f"Источник: {item.get('source', '')}\n"
            f"Кратко: {item.get('summary', '')}\n"
            f"Ссылка: {item.get('link', '')}"
        )
    return "\n\n".join(lines)


def build_fallback_digest(today, weather_block, tasks_block, news, daily):
    news_lines = []
    if news:
        for i, item in enumerate(news, start=1):
            news_lines.append(
                f"{i}. {item.get('title', '')}\n"
                f"Почему важно: показывает направление развития ИИ и автоматизации.\n"
                f"Что сделать тебе: подумать, можно ли применить это для Чинор, контента или личного ассистента."
            )
    else:
        news_lines.append("Новости не загрузились.")

    return (
        f"🌅 Доброе утро, Маруф\n\n"
        f"Сегодня {today}.\n\n"
        f"🌤 Погода\n{weather_block}\n\n"
        f"🎯 Главное сегодня\n{daily['focus']}\n\n"
        f"✅ Задачи\n{tasks_block}\n\n"
        f"🤖 Что произошло в ИИ\n" + "\n\n".join(news_lines) + "\n\n"
        f"🛠 Инструмент дня\n{daily['tool_name']}\nЗачем: {daily['tool_use']}\nТест на 20 минут: {daily['tool_test']}\n\n"
        f"🏭 Идея для Чинор\n{daily['chinor_idea']}\n\n"
        f"🎬 Идея для контента\n{daily['content_idea']}\n\n"
        f"🧠 Мысль дня\n{daily['fact']}\n\n"
        f"🎲 Карточка дня\n{daily['card']}"
    )


def generate_personal_digest(openai_client, model, today, weather_block, tasks_block, news):
    daily = pick_daily_context()
    raw_news = build_ai_news_raw(news)

    prompt = f"""
Собери утренний персональный дайджест для Маруфа на русском языке.

О Маруфе:
- Он строит мебельное производство «Чинор».
- Есть шоурум и цех, операторы распила, кромки и присадки.
- Цель: система, порядок, ответственность, меньше ручного управления, больше процессов.
- Интересы: ИИ, автоматизация, AI-видео, контент, Telegram-боты, GitHub, Railway.

Данные:
Дата: {today}

Погода:
{weather_block}

Открытые задачи:
{tasks_block}

Новости ИИ:
{raw_news}

Фокус дня: {daily['focus']}
Карточка дня: {daily['card']}
Инструмент дня: {daily['tool_name']}. Зачем: {daily['tool_use']}. Тест: {daily['tool_test']}
Идея для Чинор: {daily['chinor_idea']}
Идея для контента: {daily['content_idea']}
Факт дня: {daily['fact']}

Структура ответа:
🌅 Доброе утро, Маруф
короткое вступление 1-2 предложения

🌤 Погода
коротко по Душанбе и Худжанду. Если жара сильная, добавь практический вывод для цеха.

🎯 Главное сегодня
одна главная задача или принцип дня.

✅ Задачи
кратко по открытым задачам.

🤖 Что произошло в ИИ
3 новости. Для каждой: что произошло, почему важно, что сделать Маруфу.

🛠 Инструмент дня
название, зачем нужен, тест на 20 минут.

🏭 Идея для Чинор
одна практичная идея.

🎬 Идея для контента
один ролик или пост с хуком.

🧠 Мысль дня
одна сильная мысль руководителя.

🎲 Карточка дня
короткое действие на сегодня.

Ограничения:
- До 3500 символов.
- Без банальных лозунгов.
- Без длинных ссылок в основном тексте.
- Не придумывай факты и цифры, которых нет в данных.
"""

    try:
        response = openai_client.chat.completions.create(
            model=model,
            messages=[
                {
                    "role": "system",
                    "content": "Ты личный утренний редактор и операционный советник предпринимателя. Пиши конкретно, живо и полезно."
                },
                {"role": "user", "content": prompt},
            ],
            temperature=0.85,
            max_tokens=1200,
        )
        return response.choices[0].message.content.strip()[:3900]
    except Exception as error:
        return build_fallback_digest(today, weather_block, tasks_block, news, daily) + f"\n\n⚠️ GPT-редактор не сработал: {error}"
